k = ("Понедельник", "Вторник", "Среда", "Четверг", "Пятница", "Суббота", "Воскресенье")
hol = 0
while not (1 <= hol <=6):
    hol = int(input("Сколько выходных вы хотите? "))
    if not (1 <= hol <=6):
        print("Выходных может быть от 1 до 6")

print("Ваши выходные дни: ", k[-hol::] )
print("Ваши рабочие дни: ", k[:7-hol:])